int asciiArtLength(struct enemy_stats enemy) {
    int length = 0;

    for (int i = 0; i < 30; i++) {
        if (strcmp(enemy.ascii_art[i], "") == 0) {
            break;
        }

        length++;
    }

    return length;
}

int criticalCheckEnemy(struct enemy_stats enemy) {
    int random = (rand() % 100) + 1;

    return random <= enemy.critical_chance;
}